public class AVNotifyMessage {
    private int  ssrc;
    private int width;
    private int height;
    private byte[] img;

    public AVNotifyMessage(int ssrc, int width, int height, byte[] img) {
        this.ssrc = ssrc;
        this.width = width;
        this.height = height;
        this.img = img;
    }

    public int getSsrc() {
        return ssrc;
    }

    public void setSsrc(int ssrc) {
        this.ssrc = ssrc;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public byte[] getImg() {
        return img;
    }

    public void setImg(byte[] img) {
        this.img = img;
    }
}
